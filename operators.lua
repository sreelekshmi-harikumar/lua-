-- operators in lua
-- arithmetic operators 
local num1 = 10
local num2 = 5
print(num1+num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)
print(num1 ^ num2)
-- relational operators
print(num1 == num2)
print(num1 > num2)
print(num1 <num2)
print(num1 >= num2)
print(num1 <= num2)
print(num1~=num2)
-- logical operators
local log1 = true
local log2 = false
print( log1 and log2)
print(log1 or log2)
print(log1 and not log2)
-- assigment operators
local a = 10
local b = 5
a = a + b
print(a)
-- concatenation operators
local str1 = "Hello"
local str2 = "world"
print(str1 .. " " .. str2)
